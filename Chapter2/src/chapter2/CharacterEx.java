package chapter2;

public class CharacterEx {

	public static void main(String[] args) {
		
		char ch = 'A';
		
		System.out.println(ch);  // A
		System.out.println((int)ch); // 65
		
		ch = 66;
		
		System.out.println(ch);  // B
		
		int ch2 = 65;
		System.out.println(ch2);  // 65
		System.out.println((char)ch2);  // A
	}

}
